package Java;

public class StarterSettings {
	private String elementPageIndex;
	private String elementName;
	private String elementVarName;
	private String elementType;
	private String elementPattern;
	private String elementStyle;
	private String elementDataSource;
	private String specialAction;
	private String specialText;
	
	public String getElementPageIndex() {
		return elementPageIndex;
	}
	public void setElementPageIndex(String elementPageIndex) {
		this.elementPageIndex = elementPageIndex;
	}
	public String getElementName() {
		return elementName;
	}
	public void setElementName(String elementName) {
		this.elementName = elementName;
	}
	public String getElementVarName() {
		return elementVarName;
	}
	public void setElementVarName(String elementVarName) {
		this.elementVarName = elementVarName;
	}
	public String getElementType() {
		return elementType;
	}
	public void setElementType(String elementType) {
		this.elementType = elementType;
	}
	public String getElementPattern() {
		return elementPattern;
	}
	public void setElementPattern(String elementPattern) {
		this.elementPattern = elementPattern;
	}
	public String getElementStyle() {
		return elementStyle;
	}
	public void setElementStyle(String elementStyle) {
		this.elementStyle = elementStyle;
	}
	public String getElementDataSource() {
		return elementDataSource;
	}
	public void setElementDataSource(String elementDataSource) {
		this.elementDataSource = elementDataSource;
	}
	public String getSpecialAction() {
		return specialAction;
	}
	public void setSpecialAction(String specialAction) {
		this.specialAction = specialAction;
	}
	public String getSpecialText() {
		return specialText;
	}
	public void setSpecialText(String specialText) {
		this.specialText = specialText;
	}
}
