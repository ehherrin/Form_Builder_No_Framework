package Java;

public class NewDevSettings {
	private String newDevName;
	private String newMaxQty;
	private String newVarName;
	private String editVarName;
	private String grpId;
	private String busReqGrpId;
	
	public String getNewDevName() {
		return newDevName;
	}
	public void setNewDevName(String newDevName) {
		this.newDevName = newDevName;
	}
	
	public String getNewMaxQty() {
		return newMaxQty;
	}
	public void setNewMaxQty(String newMaxQty) {
		this.newMaxQty = newMaxQty;
	}
	
	public String getNewVarName() {
		return newVarName;
	}
	public void setNewVarName(String newVarName) {
		this.newVarName = newVarName;
	}
	
	public String getEditVarName() {
		return editVarName;
	}
	public void setEditVarName(String editVarName) {
		this.editVarName = editVarName;
	}
	
	public String getGrpId() {
		return grpId;
	}
	public void setGrpId(String grpId) {
		this.grpId = grpId;
	}
	
	public String getBusReqGrpId() {
		return busReqGrpId;
	}
	public void setBusReqGrpId(String busReqGrpId) {
		this.busReqGrpId = busReqGrpId;
	}
}
