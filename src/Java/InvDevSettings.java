package Java;

public class InvDevSettings {
	private String invDevName;
	private String invMaxQty;
	private String invVarName;
	private String grpId;
	private String busReqGrpId;
	
	public String getInvDevName() {
		return invDevName;
	}
	public void setInvDevName(String invDevName) {
		this.invDevName = invDevName;
	}
	
	public String getInvMaxQty() {
		return invMaxQty;
	}
	public void setInvMaxQty(String invMaxQty) {
		this.invMaxQty = invMaxQty;
	}
	
	public String getInvVarName() {
		return invVarName;
	}
	public void setInvVarName(String invVarName) {
		this.invVarName = invVarName;
	}
	
	public String getGrpId() {
		return grpId;
	}
	public void setGrpId(String grpId) {
		this.grpId = grpId;
	}
	
	public String getBusReqGrpId() {
		return busReqGrpId;
	}
	public void setBusReqGrpId(String busReqGrpId) {
		this.busReqGrpId = busReqGrpId;
	}
	
}
